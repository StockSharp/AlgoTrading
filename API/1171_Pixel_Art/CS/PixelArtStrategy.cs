/// <summary>
/// Strategy that prints pixel art logos to the log.
/// </summary>
public class PixelArtStrategy : Strategy
{
	private readonly StrategyParam<string> _logo;

	public string Logo
	{
		get => _logo.Value;
		set => _logo.Value = value;
	}

	public PixelArtStrategy()
	{
		_logo = Param(nameof(Logo), "pAulse")
			.SetDisplay("Logo", "Pixel art logo to display", "General");
	}

	protected override void OnStarted(DateTimeOffset time)
	{
		base.OnStarted(time);

		if (!_pixels.TryGetValue(Logo, out var colors))
		{
			LogError($"Unknown logo '{Logo}'.");
			return;
		}

		var width = (int)Math.Sqrt(colors.Length);
		for (var r = 0; r < width; r++)
		{
			var row = string.Join(" ", colors.Skip(r * width).Take(width));
			LogInfo(row);
		}
	}

